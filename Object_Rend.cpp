#include "Object_Rend.h"

ObjectRenderer::ObjectRenderer(const Shader &shader)
{
	this->shader = shader;
    this->initCubeRenderData();
    this->initSphereRenderData();
}

ObjectRenderer::~ObjectRenderer()
{
    glDeleteVertexArrays(1, &this->cubeVAO);
}

