void ObjectRenderer::DrawCube(Texture2D &texture, glm::vec3 position, glm::vec3 size, float rotate, glm::vec3 axis, bool color, glm::vec3 objectColor)
{
    // prepare transformations
    this->shader.Use();
    glm::mat4 model = glm::mat4(1.0f);
    model = glm::translate(model, position);  
    model = glm::rotate(model, glm::radians(rotate), axis); 
    model = glm::scale(model, size); 
  
    this->shader.SetMatrix4("model", model);
    this->shader.SetVector3f("objectColor", objectColor);
    this->shader.SetBool("color", color);
    this->shader.SetBool("light", false);
  
    glActiveTexture(GL_TEXTURE0);
    texture.Bind();

    glBindVertexArray(this->cubeVAO);
    glDrawArrays(GL_TRIANGLES, 0, 36);
    glBindVertexArray(0);
}  

void ObjectRenderer::DrawSphere(glm::vec3 position, float radius, glm::vec3 color, bool light)
{
    // prepare transformations
    this->shader.Use();
    glm::mat4 model = glm::mat4(1.0f);
    model = glm::translate(model, position);
    model = glm::scale(model, glm::vec3(radius)); 
  
    this->shader.SetMatrix4("model", model);
    this->shader.SetVector3f("objectColor", color);
    this->shader.SetBool("color", true);
    this->shader.SetBool("light", light);

    glBindVertexArray(this->sphereVAO);
    glDrawElements(GL_TRIANGLE_STRIP, this->indexCount, GL_UNSIGNED_INT, 0);
    glBindVertexArray(0);
}  

